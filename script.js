/* ============================================================
   RGB PREVIEW (ENABLE BUTTONS + EFFECTS)
   ============================================================ */

function updateRGBPreview() {
    const mode = document.getElementById("rgbMode").value;

    const c1 = document.getElementById("color1").value;
    const c2 = document.getElementById("color2").value;
    const c3 = document.getElementById("color3").value;

    const e1 = document.getElementById("enable1").checked;
    const e2 = document.getElementById("enable2").checked;
    const e3 = document.getElementById("enable3").checked;

    const effect = document.getElementById("rgbEffect").value;
    const preview = document.getElementById("rgbPreview");

    // Effekte resetten
    preview.classList.remove("rgb-morph", "rgb-pulse", "rgb-breathe", "rgb-rainbow");

    // Aktive Farben sammeln
    const active = [];
    if (e1) active.push(c1);
    if (e2) active.push(c2);
    if (e3) active.push(c3);

    // Falls keine aktiv → Color1 fallback
    if (active.length === 0) active.push(c1);

    // Hintergrund setzen
    if (mode === "single" || active.length === 1) {
        preview.style.background = active[0];
    } else {
        preview.style.background = `linear-gradient(90deg, ${active.join(", ")})`;
    }

    // Effekt anwenden
    if (effect === "morph") preview.classList.add("rgb-morph");
    if (effect === "pulse") preview.classList.add("rgb-pulse");
    if (effect === "breathe") preview.classList.add("rgb-breathe");
    if (effect === "rainbow") preview.classList.add("rgb-rainbow");

    showRGBSaveButton();
}


/* ============================================================
   SHOW SAVE BUTTON
   ============================================================ */

function showRGBSaveButton() {
    document.getElementById("btn-save-rgb").classList.remove("hidden");
}


/* ============================================================
   SAVE RGB SETTINGS
   ============================================================ */

function saveRGB() {
    const data = {
        mode: document.getElementById("rgbMode").value,
        effect: document.getElementById("rgbEffect").value,
        c1: document.getElementById("color1").value,
        c2: document.getElementById("color2").value,
        c3: document.getElementById("color3").value,
        e1: document.getElementById("enable1").checked,
        e2: document.getElementById("enable2").checked,
        e3: document.getElementById("enable3").checked
    };

    localStorage.setItem("rgbSettings", JSON.stringify(data));

    showSaveMessage("saveMessageSettings", "RGB gespeichert.");
}


/* ============================================================
   LOAD RGB SETTINGS
   ============================================================ */

function loadRGB() {
    const raw = localStorage.getItem("rgbSettings");
    if (!raw) return;

    const data = JSON.parse(raw);

    document.getElementById("rgbMode").value = data.mode;
    document.getElementById("rgbEffect").value = data.effect;

    document.getElementById("color1").value = data.c1;
    document.getElementById("color2").value = data.c2;
    document.getElementById("color3").value = data.c3;

    document.getElementById("enable1").checked = data.e1;
    document.getElementById("enable2").checked = data.e2;
    document.getElementById("enable3").checked = data.e3;

    updateRGBPreview();
}
