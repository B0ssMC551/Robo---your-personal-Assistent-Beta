/* ============================================================
   APPLY LANGUAGE + SELECT OPTION FIX
   ============================================================ */

function applyLanguage(lang) {
    const dict = i18n[lang] || i18n["en"];

    document.querySelectorAll("[data-i18n]").forEach(el => {
        const key = el.getAttribute("data-i18n");
        if (dict[key]) el.textContent = dict[key];
    });

    translateSelectOptions(lang);
}

function translateSelectOptions(lang) {
    const onb = document.getElementById("onb-language");
    const home = document.getElementById("home-language-select");

    if (onb) {
        onb.options[0].text = "Deutsch";
        onb.options[1].text = "English";
        onb.options[2].text = "Español";
        onb.options[3].text = "Français";
        onb.options[4].text = "Italiano";
    }

    if (home) {
        home.options[0].text = "Deutsch";
        home.options[1].text = "English";
        home.options[2].text = "Español";
        home.options[3].text = "Français";
        home.options[4].text = "Italiano";
    }
}


/* ============================================================
   ONBOARDING LOGIC
   ============================================================ */

function onLanguageChange() {
    const lang = document.getElementById("onb-language").value;
    localStorage.setItem("language", lang);
    applyLanguage(lang);
    document.getElementById("btn-language-next").classList.remove("disabled");
}

function validateWakeOnb() {
    const val = document.getElementById("onb-wakeword").value.trim();
    document.getElementById("btn-wake-next").classList.toggle("disabled", val.length === 0);
}

function nextStep(current, next) {
    document.getElementById(current).style.display = "none";
    document.getElementById(next).style.display = "block";
}

function skipOnboarding() {
    localStorage.setItem("setupDone", "true");
    if (!localStorage.getItem("language")) localStorage.setItem("language", "de");
    if (!localStorage.getItem("wakeword")) localStorage.setItem("wakeword", "Robo");

    document.getElementById("onboarding").style.display = "none";
    document.getElementById("app").style.display = "block";

    initApp();
    showPage("home");
}

function finishSetup() {
    const lang = localStorage.getItem("language") || "de";
    const wake = document.getElementById("onb-wakeword").value.trim() || "Robo";

    localStorage.setItem("setupDone", "true");
    localStorage.setItem("language", lang);
    localStorage.setItem("wakeword", wake);

    document.getElementById("onboarding").style.display = "none";
    document.getElementById("app").style.display = "block";

    initApp();
    showPage("home");
}


/* ============================================================
   INITIAL LOAD
   ============================================================ */

window.onload = () => {
    const lang = localStorage.getItem("language") || "de";
    applyLanguage(lang);

    if (localStorage.getItem("setupDone") === "true") {
        document.getElementById("app").style.display = "block";
        initApp();
        showPage("home");
    } else {
        document.getElementById("onboarding").style.display = "block";
        document.getElementById("step-language").style.display = "block";
    }
};


/* ============================================================
   INIT APP
   ============================================================ */

function initApp() {
    const lang = localStorage.getItem("language") || "de";
    applyLanguage(lang);

    const homeSelect = document.getElementById("home-language-select");
    if (homeSelect) homeSelect.value = lang;

    const wake = localStorage.getItem("wakeword") || "Robo";
    document.getElementById("home-wakeword").textContent = wake;

    const voice = localStorage.getItem("roboVoice");
    if (voice) document.getElementById("voiceSelect").value = voice;

    const savedWake = localStorage.getItem("roboWakeword") || wake;
    document.getElementById("customWake").value = savedWake;

    ["whatsapp","calls","music","apps","camera","mic","notify","kids"].forEach(loadToggle);

    const whatsappOn = localStorage.getItem("toggle-whatsapp") === "1";
    document.getElementById("home-whatsapp").textContent = whatsappOn ? "Aktiv" : "Inaktiv";

    loadRGB();
}


/* ============================================================
   NAVIGATION
   ============================================================ */

function showPage(page) {
    document.querySelectorAll(".page").forEach(p => p.style.display = "none");
    document.getElementById("page-" + page).style.display = "block";

    document.querySelectorAll("#topnav .nav-right button")
        .forEach(b => b.classList.remove("active"));

    document.getElementById("nav-" + page).classList.add("active");
}


/* ============================================================
   AUTO HIDE NAVBAR
   ============================================================ */

let lastScroll = 0;

window.addEventListener("scroll", () => {
    const current = window.scrollY;
    const nav = document.getElementById("topnav");

    if (current > lastScroll + 5) nav.classList.add("nav-hidden");
    else if (current < lastScroll - 5) nav.classList.remove("nav-hidden");

    lastScroll = current;
});


/* ============================================================
   TOGGLES
   ============================================================ */

function saveToggle(key) {
    const el = document.getElementById("toggle-" + key);
    localStorage.setItem("toggle-" + key, el.checked ? "1" : "0");

    if (key === "whatsapp") {
        document.getElementById("home-whatsapp").textContent =
            el.checked ? "Aktiv" : "Inaktiv";
    }
}

function loadToggle(key) {
    const val = localStorage.getItem("toggle-" + key);
    const el = document.getElementById("toggle-" + key);
    if (el) el.checked = (val === "1");
}


/* ============================================================
   WHATSAPP AUTO OPEN
   ============================================================ */

function toggleWhatsApp() {
    const el = document.getElementById("toggle-whatsapp");
    saveToggle("whatsapp");

    if (el.checked) window.open("https://web.whatsapp.com", "_blank");
}


