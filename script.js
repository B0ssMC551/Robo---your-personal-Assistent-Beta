function setPreset(name) {
    const presets = {
        warm:  ["#ff9f0a", "#ff3b30", "#ffd60a"],
        cool:  ["#0a84ff", "#30d158", "#5e5ce6"],
        sunset:["#ff3b30", "#ff9f0a", "#ff2d55"],
        neon:  ["#ff2d55", "#5e5ce6", "#30d158"]
    };

    const [c1, c2, c3] = presets[name] || presets.cool;

    document.getElementById("color1").value = c1;
    document.getElementById("color2").value = c2;
    document.getElementById("color3").value = c3;

    updateRGBPreview();
    showRGBSaveButton();
}


